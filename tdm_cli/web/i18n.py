def _lang_dir() -> Path:
    from constants import LANG_PATH

    return Path(LANG_PATH)


@lru_cache(maxsize=1)
def available_languages() -> list[dict[str, str]]:
    """List of ``{code, name}`` for every upstream lang file, sorted by name.

    ``code`` is the file stem (the value passed to ``GET /i18n/<code>``);
    ``name`` is the language's own ``english_name`` where present, else the stem.
    """
    langs: list[dict[str, str]] = []
    d = _lang_dir()
    if d.is_dir():
        for path in sorted(d.glob("*.json")):
            name = path.stem
            try:
                data = json.loads(path.read_text(encoding="utf8"))
                name = data.get("english_name") or path.stem
            except (OSError, ValueError):
                pass
            langs.append({"code": path.stem, "name": name})
    if not langs:
        langs = [{"code": "English", "name": "English"}]
    return langs


def _get(d: dict[str, Any], dotted: str) -> str | None:
    cur: Any = d
    for part in dotted.split("."):
        if not isinstance(cur, dict) or part not in cur:
            return None
        cur = cur[part]
    return cur if isinstance(cur, str) else None


