@lru_cache(maxsize=32)
def catalogue(code: str) -> dict[str, str]:
    """Flat ``{key: text}`` catalogue for one language code.

    Merge order (later wins): English upstream + English extras (baseline
    fallback) → requested-language upstream → requested-language extras.
    """
    d = _lang_dir()

    def load(name: str) -> dict[str, Any]:
        p = d / f"{name}.json"
        try:
            return json.loads(p.read_text(encoding="utf8"))
        except (OSError, ValueError):
            return {}

    en_up = load("English")
    req_up = load(code) if code != "English" else en_up

    out: dict[str, str] = {}
    # 1. English baseline for every key (guarantees no missing string)
    out.update(_EXTRA["English"])
    for webkey, dotted in _UPSTREAM_MAP.items():
        val = _get(en_up, dotted)
        if val is not None:
            out[webkey] = val
    # 2. requested language upstream translations
    for webkey, dotted in _UPSTREAM_MAP.items():
        val = _get(req_up, dotted)
        if val is not None:
            out[webkey] = val
    # 3. requested language WebUI-specific extras
    if code in _EXTRA:
        out.update(_EXTRA[code])
    # strip trailing ": " / " " that upstream uses for inline labels
    return {k: v.rstrip(": ").strip() if isinstance(v, str) else v for k, v in out.items()}


def strings_for(lang: str) -> dict[str, Any]:
    """Public alias for :func:`catalogue` — the flat catalogue served to a
    browser via ``GET /i18n/<lang>``. Unknown codes fall back to English."""
    return catalogue(lang)


def default_language() -> str:
    """Language code the UI should start on before the user picks one.

    Follows the miner's own ``settings.language`` if that lang file exists,
    else English. The browser still overrides this from ``navigator`` /
    localStorage — this is only the server-side default hint."""
    codes = {entry["code"] for entry in available_languages()}
    try:
        from constants import SETTINGS_PATH
        from utils import json_load

        data = json_load(SETTINGS_PATH, {})
        lang = data.get("language") if isinstance(data, dict) else None
        if lang in codes:
            return lang
    except Exception:
        pass
    return "English" if "English" in codes else next(iter(sorted(codes)))
